#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] && argv[1][i] != 'a')
			i++;
		if (argv[1][i] == '\0')
			return (write(1, "\n", 1));
	}
	write(1, "a\n", 1);
	return (0);
}
